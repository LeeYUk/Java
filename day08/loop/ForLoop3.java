package loop;

public class ForLoop3 {

	public static void main(String[] args) {
		//1
		for (int i = 0; i <10; i++) {
			System.out.print("*");
		}
		System.out.println("\n");
		
		//2
		for (int i = 0; i <5; i++) {
			System.out.print("커피*");
		}
		
		//3
		for (int i = 0; i <4; i++) {
			System.out.println("커피*우유");
		}
		//4
		for (int i = 0; i <3; i++) {
			System.out.println((i+1)+" : 짱!");
		}




	}
	
	
}
