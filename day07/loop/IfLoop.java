package loop;

public class IfLoop {

	public static void main(String[] args) {
		//if 문
		
		int x=0;
		//비교연산자(==,!=,>,>=)로 비교할수 있는 것은 기본형 데이터만 가능
		if(x==0) {
			//조건을 사용할때는 비교하는 것으로쓰고, 결과는 무조건 true/false 
			System.out.println("ㅎㅇ");//true
		}
		
		else if (x==1) {//false
			System.out.println("ㅎㅇ");
		}

	}

}
