package review;

public class Basic2 {

	public static void main(String[] args) {
		// 기본 심화2

		//기본형 4가지
		//정수형 자료형
		//-128~127, 1byte =byte
		//-3만~3만 ,2byte  =short
		//-21억~-21억, 4byte =int
		//21억 이상, 8byte = long
		//정수형 예제
		byte age=127;
		short days=1000;
		int money=100000;
		long bank=2300000000L; //long타입 에러 발생시 숫자뒤에 L,l 붙이기
		
		//실수형 자료형 2가지
		//소수점 4자리까지 ,4byte =float
		//소수점 4자리 이상 ,8byte =double
		//실수형 예제
		float height=170.4f;//float타입 에러시 숫자 뒤에 F,f붙이기
		double weight=43.555555;
		
		
		//문자1 char
		//논리형 boolean
		
	}

}
